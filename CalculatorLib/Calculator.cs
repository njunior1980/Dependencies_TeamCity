using System;

namespace CalculatorLib
{
    public class Calculator
    {
        public int Sum(int n1, int n2) => n1 + n2;
        public int Subtract(int n1, int n2) => n1 - n2;
        public int Multiply(int n1, int n2) => n1 * n2;
        public int Divide(int n1, int n2) => n1 / n2;
    }
}
